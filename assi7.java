package assi;

public class assi7 {
	

	    public static void main(String[] args) {

	        int num = 10;
	        long factorial = 3;
	        for(int i = 1; i <= num; ++i)
	        {
	            // factorial = factorial * i;
	            factorial *= i;
	        }
	        System.out.printf("Factorial of %d = %d", num, factorial);
	    }
	}


